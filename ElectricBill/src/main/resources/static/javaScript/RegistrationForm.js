window.onload = function () {
	
	
	//Get html elements
	var divisionSel = document.getElementById("divisionSel");
	var districtSel = document.getElementById("districtSel");	
	var upazilaSel = document.getElementById("upazilaSel");
	
	
	//Load division
	for (var division in countryInfo) {
		divisionSel.options[divisionSel.options.length] = new Option(division, division);
	}
	
	//division Changed
	divisionSel.onchange = function () {
		 
		 districtSel.length = 1; // remove all options bar first
		 upazilaSel.length = 1; // remove all options bar first
		 
		 
		 if (this.selectedIndex < 1)
			 return; // done
		 
		 for (var district in countryInfo[this.value]) {
			 districtSel.options[districtSel.options.length] = new Option(district, district);
		 }
	}
	
	
	
	//district Changed
	districtSel.onchange = function () {
		upazilaSel.length = 1; // remove all options bar first
		
		if (this.selectedIndex < 1)
			return; // done
		
		var upazilas = countryInfo[divisionSel.value][this.value];
		for (var i = 0; i < upazilas.length; i++) {
			upazilaSel.options[upazilaSel.options.length] = new Option(upazilas[i], upazilas[i]);
		}
	}	
}